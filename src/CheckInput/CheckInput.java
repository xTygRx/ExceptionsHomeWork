package CheckInput;

public class CheckInput extends CheckInput_abstract{
    @Override
    public boolean checkLength(String input) {
        return super.checkLength(input);
    }

    @Override
    public boolean checkDataInput(String[] data) {
        return super.checkDataInput(data);
    }

    @Override
    public boolean check(String name) {
        return super.check(name);
    }
}
